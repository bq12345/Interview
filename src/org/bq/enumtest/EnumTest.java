public class EnumTest {

	public static void main(String[] args) {
		Color color = Color.RED;
		switch (color) {
		case BLANK:
			System.out.println(color);
			break;
		case RED:
			System.out.println(color);
			break;

		case GREEN:
			System.out.println(color);
			break;

		case YELLOW:
			System.out.println(color);
			break;

		}
	}

}
